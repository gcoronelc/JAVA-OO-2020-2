package pe.uni.proysuma.service;

public class SumaService {

	/**
	 * Calcula la suma de dos números.
	 * @param n1 Número 1.
	 * @param n2 Número 2.
	 * @return Retorna la suma de n1 y n2.
	 */
	public int sumar(int n1, int n2){
		// Variables
		int suma;
		// Proceso
		suma = n1 + n2;
		// Reporte
		return suma;
	}
	
}
